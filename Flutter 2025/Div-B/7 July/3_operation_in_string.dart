void main() {
  print("Addition of 10 and 20 is ${10+20}");
  print("Subtraction of 20 from 30 is ${30-20}");
  print("Multiplication of 5 and 6 is ${5*6}");
  print("Division of 20 by 4 is ${20/4}");
  print("Modulus of 10 and 3 is ${10%3}");
  
  // Using variables in string interpolation
  int likes = 10;
  print("I like Flutter ${likes+1} times");
}